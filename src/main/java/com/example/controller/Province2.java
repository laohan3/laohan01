package com.example.controller;

public class Province2 {
    private Integer id;
    private String name;

    public Province2(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Province2() {
    }

    public int getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
